package ch04_while;

public class PrintFExam {
	public static void main(String[] args) {
		// %d int타입 정수,
		// %f double타입의 실수
		// %e 부동소수점 형태
		// %c 문자
		// %s String타입의 문자열
		System.out.printf("%d \n",15);
		System.out.printf("%f \n",15.0);
		System.out.printf("%e \n",15.0);
		System.out.printf("%c \n",65);
		System.out.printf("%s \n",15);
	}
}
